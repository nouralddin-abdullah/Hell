export function containsBadWords(comment: string) {
  const normalizedComment = comment.toLowerCase();
  const text = normalizedComment
    .replace("أ", "ا")
    .replace("إ", "ا")
    .replace("ٌ", "")
    .replace("ُ", "")
    .replace("ً", "")
    .replace("َ", "")
    .replace("ٍ", "")
    .replace("ْ", "")
    .replace("ّ", "")
    .replace("ِ", "")
    .replace(".", "");

  for (const word of badWords) {
    if (text.includes(word.toLowerCase())) {
      return true;
    }
  }

  return false;
}
